# Descrição da Solicitação: Contador Decrescente: Desenvolva um programa que imprima na tela todos os números inteiros de 50 a 1, em ordem decrescente.

# Contador de 50 até 1
numero = 50

# Loop para imprimir decrescente
while numero >= 1:
    print(numero)
    numero -= 1
